	function hexCopy1() {
		/* Get the text field */
		var copyText = document.getElementById("hex1");

		/* Select the text field */
		copyText.select();

		/* Copy the text inside the text field */
		document.execCommand("Copy");

		/* Alert the copied text */
		alert("Copied the text: " + copyText.value);
	}
	
